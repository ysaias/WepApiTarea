public class Tarea {

   private String tarea;
    private String completada;

    public Tarea() {

    }

    public String getTarea() {
        return tarea;
    }

    public void setTarea(String tarea) {
        this.tarea = tarea;
    }

    public String completada() {
        return completada;
    }

    public void setCompletada(String completada) {
        this.completada = completada;
    }
}
